let passive_0 ="he said \"What R U doing?\""; // using double quotes escape characters
let passive_1 ='he said \'when R U going?\''; // using single quotes escape characters
let passive_2 ='he said\n\'when R U comming?\''; // using new line escape character
let passive_3 =`he said\t\'R U coming late?\'`;  // using tab escape character
console.log(`${passive_0}\n${passive_1}\n${passive_2}\n${passive_3}`); // printing the strings



